SELECT CUSTOMER#, LASTNAME, STATE FROM CUSTOMERS WHERE STATE IN ('GA', 'NJ') ORDER BY LASTNAME ASC;

SELECT CUSTOMER#, LASTNAME, STATE FROM CUSTOMERS WHERE STATE = 'GA' OR STATE = 'NJ' ORDER BY LASTNAME ASC;

