REM Hands-On #9
SELECT ISBN, TITLE FROM BOOKS WHERE SUBSTR(TITLE, 2, 1) = 'A' AND SUBSTR(TITLE, 4, 1) = 'N' ORDER BY TITLE DESC;

REM Hands-On #10
SELECT TITLE, PUBDATE FROM BOOKS WHERE PUBDATE BETWEEN '01-JAN-05' AND '31-DEC-05' AND CATEGORY LIKE '%COMPUTER%';

SELECT TITLE, PUBDATE FROM BOOKS WHERE EXTRACT(YEAR FROM PUBDATE) = 2005 AND CATEGORY LIKE '%COMPUTER%';

SELECT TITLE, PUBDATE FROM BOOKS WHERE TO_CHAR(PUBDATE, 'YYYY') = '2005' AND CATEGORY LIKE '%COMPUTER%';

